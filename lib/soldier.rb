class Soldier

	def initialize(name)
		@name = name
		@health = 100
		@strength = 5
		@agility = 5
		@intelligence = 5
		@wisdom = 5
		@dexterity = 5
	end
	
	def name
		@name		
	end

	def health
		@health		
	end

	def strength
		@strength		
	end

	def agility
		@agility		
	end

	def intelligence
		@intelligence
	end

	def wisdom
		@wisdom		
	end

	def dexterity
		@dexterity
	end
end


